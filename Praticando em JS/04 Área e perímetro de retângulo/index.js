// Receba valores de lado de um retângulo e exbiba os valores de área e perimetro

let base = 10; // 1° dos 2 valores do retângulo
let altura = 10; // 2° dos 2 valores do retângulo
const área = base * altura;
const perimetro = 2 * (base + altura);

console.log(
  `Um retângulo com base ${base} e altura ${altura} tem a área = ${área} e perimetro = ${perimetro}.`
);
